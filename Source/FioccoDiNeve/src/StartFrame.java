    private void openSnowflakeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openSnowflakeButtonActionPerformed
        SnowflakeFrame sf = new SnowflakeFrame();
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("."));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("snowflake files (*.flake)", "flake");
        chooser.addChoosableFileFilter(filter);
        chooser.setFileFilter(filter);
        chooser.setDialogTitle("Import points");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            Path origine = chooser.getSelectedFile().toPath();
            try {
                Saver s = new Saver(origine);
                sf.snowflakePanel1.setTrianglePoints(s.getTrianglePoints());
                sf.snowflakePanel1.setCutPoints(s.getCutPoints());
                sf.setVisible(true);
                this.dispose();
            }catch (IOException ioe) {
                showMessageDialog(null, "Impossibile leggere il file.");
            }
        } 
    }//GEN-LAST:event_openSnowflakeButtonActionPerformed

